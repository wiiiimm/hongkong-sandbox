#!/usr/bin/env python3
"""
Generate a rotatable 3D Lantau Island terrain object and browser viewer.

The mesh uses the same cached Terrarium DEM tiles and approximate Lantau mask
as the skyline generator. Outputs:
- lantau-terrain-mesh.json for the Three.js viewer
- lantau-terrain.obj for DCC/CAD import
- lantau-3d-viewer.html for local interactive viewing
"""

from __future__ import annotations

import json
import math
from pathlib import Path

import numpy as np

import generate_lantau_skyline as skyline


OUT = Path(__file__).resolve().parent
GRID_X = 220
GRID_Z = 140
VERTICAL_EXAGGERATION = 2.0
BASE_DEPTH_M = 80


def bilinear_sample(grid: np.ndarray, extent: dict[str, float], lon: float, lat: float) -> float:
    h, w = grid.shape
    x = (lon - extent["west"]) / (extent["east"] - extent["west"]) * (w - 1)
    y = (extent["north"] - lat) / (extent["north"] - extent["south"]) * (h - 1)
    if x < 0 or y < 0 or x >= w - 1 or y >= h - 1:
        return 0.0
    x0 = int(math.floor(x))
    y0 = int(math.floor(y))
    dx = x - x0
    dy = y - y0
    a = grid[y0, x0]
    b = grid[y0, x0 + 1]
    c = grid[y0 + 1, x0]
    d = grid[y0 + 1, x0 + 1]
    return float((a * (1 - dx) + b * dx) * (1 - dy) + (c * (1 - dx) + d * dx) * dy)


def build_mesh() -> dict[str, object]:
    dem, extent = skyline.build_dem_mosaic()
    lon_values = np.linspace(skyline.BBOX["west"], skyline.BBOX["east"], GRID_X)
    lat_values = np.linspace(skyline.BBOX["south"], skyline.BBOX["north"], GRID_Z)
    mean_lat = (skyline.BBOX["south"] + skyline.BBOX["north"]) / 2.0
    metres_per_degree_lon = 111_320.0 * math.cos(math.radians(mean_lat))
    metres_per_degree_lat = 110_540.0
    centre_lon = (skyline.BBOX["west"] + skyline.BBOX["east"]) / 2.0
    centre_lat = (skyline.BBOX["south"] + skyline.BBOX["north"]) / 2.0

    index_grid: list[list[int | None]] = []
    vertices: list[float] = []
    heights: list[float] = []
    lon_lat: list[tuple[float, float]] = []

    for lat in lat_values:
        row: list[int | None] = []
        for lon in lon_values:
            if skyline.point_in_polygon(float(lon), float(lat), skyline.LANTAU_POLYGON):
                elev = max(0.0, bilinear_sample(dem, extent, float(lon), float(lat)))
                x = (lon - centre_lon) * metres_per_degree_lon
                z = -(lat - centre_lat) * metres_per_degree_lat
                y = elev * VERTICAL_EXAGGERATION
                row.append(len(heights))
                vertices.extend([round(x, 3), round(y, 3), round(z, 3)])
                heights.append(round(elev, 3))
                lon_lat.append((float(lon), float(lat)))
            else:
                row.append(None)
        index_grid.append(row)

    indices: list[int] = []
    for zi in range(GRID_Z - 1):
        for xi in range(GRID_X - 1):
            a = index_grid[zi][xi]
            b = index_grid[zi][xi + 1]
            c = index_grid[zi + 1][xi]
            d = index_grid[zi + 1][xi + 1]
            if a is not None and b is not None and c is not None and d is not None:
                indices.extend([a, c, b, b, c, d])

    x_vals = vertices[0::3]
    y_vals = vertices[1::3]
    z_vals = vertices[2::3]
    metadata = {
        "agent": "Codex",
        "terrain_source": "Cached AWS elevation-tiles-prod Terrarium DEM tiles, zoom 13.",
        "projection": "Local metre coordinates; x west-east, z south-north, y elevation.",
        "vertical_exaggeration": VERTICAL_EXAGGERATION,
        "grid": {"x_samples": GRID_X, "z_samples": GRID_Z},
        "vertex_count": len(heights),
        "triangle_count": len(indices) // 3,
        "bbox_lon_lat": skyline.BBOX,
        "bounds_m": {
            "x_min": min(x_vals),
            "x_max": max(x_vals),
            "y_min": min(y_vals),
            "y_max": max(y_vals),
            "z_min": min(z_vals),
            "z_max": max(z_vals),
        },
        "notes": [
            "Approximate Lantau coastline mask removes surrounding sea and most nearby islands.",
            "Elevation is vertically exaggerated in the 3D object for legibility; raw metre height is preserved in the heights_m array.",
            "No buildings, vegetation, or man-made structures are included.",
        ],
    }
    return {
        "metadata": metadata,
        "vertices": vertices,
        "indices": indices,
        "heights_m": heights,
        "lon_lat": [[round(a, 7), round(b, 7)] for a, b in lon_lat],
    }


def write_obj(mesh: dict[str, object]) -> None:
    vertices = mesh["vertices"]
    indices = mesh["indices"]
    assert isinstance(vertices, list)
    assert isinstance(indices, list)
    lines = [
        "# Lantau Island terrain mesh",
        "# Generated by codex/generate_lantau_3d.py",
        f"# Vertical exaggeration: {VERTICAL_EXAGGERATION}",
    ]
    for i in range(0, len(vertices), 3):
        lines.append(f"v {vertices[i]} {vertices[i + 1]} {vertices[i + 2]}")
    for i in range(0, len(indices), 3):
        # OBJ indices are 1-based.
        lines.append(f"f {indices[i] + 1} {indices[i + 1] + 1} {indices[i + 2] + 1}")
    (OUT / "lantau-terrain.obj").write_text("\n".join(lines) + "\n", encoding="utf-8")


