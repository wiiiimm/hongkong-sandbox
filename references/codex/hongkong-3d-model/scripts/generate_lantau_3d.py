def main() -> None:
    mesh = build_mesh()
    (OUT / "lantau-terrain-mesh.json").write_text(
        json.dumps(mesh, separators=(",", ":")), encoding="utf-8"
    )
    write_obj(mesh)
    write_viewer()


if __name__ == "__main__":
    main()
